    private void jbtnequalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnequalActionPerformed
        String ans;
        secondnum = Double.parseDouble(jtxtdisplay.getText());
        if(result != 0)
            firstnum = result;
        if(operations == "+"){
            result = firstnum + secondnum;
            ans = String.format("%.2f", result);
            jtxtdisplay.setText(ans);
        }
        else if(operations == "-"){
            result = firstnum - secondnum;
            ans = String.format("%.2f", result);
            jtxtdisplay.setText(ans);
        }
        else if(operations == "*"){
            result = firstnum * secondnum;
            ans = String.format("%.2f", result);
            jtxtdisplay.setText(ans);
        }
        else if(operations == "/"){
            result = firstnum / secondnum;
            ans = String.format("%.2f", result);
            jtxtdisplay.setText(ans);
        }
        else if(operations == "%"){
            result = firstnum % secondnum;
            ans = String.format("%.2f", result);
            jtxtdisplay.setText(ans);
        }
        else if(operations == "x^y"){
            result = Math.pow(firstnum, secondnum);
            ans = String.format("%.2f", result);
            jtxtdisplay.setText(ans);
        }
        else if(operations == "yroot"){
            result = Math.pow(firstnum, 1.0/secondnum);
            ans = String.format("%.2f", result);
            jtxtdisplay.setText(ans);
        }
        else if(operations == "summation"){
            for(double i = firstnum; i <= secondnum;i++)
                result += i;
            ans = String.format("%.2f", result);
            jtxtdisplay.setText(ans);
        }
    }//GEN-LAST:event_jbtnequalActionPerformed

