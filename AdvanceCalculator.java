    private void jbtn7KeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jbtn7KeyTyped
        char c = evt.getKeyChar();
        if(c == KeyEvent.VK_7){
            String num = jtxtdisplay.getText();
        if(num.equals("0"))
            num = jbtn7.getText();
        else
            num = jtxtdisplay.getText() + jbtn7.getText();        
        jtxtdisplay.setText(num);
        }
    }//GEN-LAST:event_jbtn7KeyTyped

